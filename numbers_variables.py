x = 9
y = 5
# Arithmetic operators
print(f"division: {x/y}")
print(f"multiply: {x*y}")
print(f"subtract: {x-y}")
print(f"modulo: {x%y}")
print(f"power: {y**3}")
print(f"2+10*10+3: {2+10*10+3}")
print(f"(2+10)*(10+3): {(2+10)*(10+3)}")

x=10
print(f"now x is {x}")
